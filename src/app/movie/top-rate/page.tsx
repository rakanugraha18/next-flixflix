import Link from "next/link";

type MoviePageProps = { params: { slug: string } };

async function getData() {
  const res = await fetch(
    `${process.env.NEXT_APP_BASEURL}/movie/top_rated?api_key=${process.env.NEXT_APP_API_KEY}`
  );

  if (!res.ok) {
    throw new Error("Failed to fetch data");
  }

  return res.json();
}
